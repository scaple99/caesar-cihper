def encrypt(message, key):
    code =  ""
    for letter in message:
        letter_index=alphabet.index(letter)
        letter_index+=key
        
        if letter_index > 25:
            letter_index=0
            letter_index+=key

        letter=alphabet[letter_index]
        code+=letter
        
    print(f"Your encrypted message is {code}, the receiptient must use {key} to decrypt it")

